package com.zeni.day03;

public class Slope {

  private int moveX;
  private int moveY;

  public Slope(int i, int j) {
    this.moveX = i;
    this.moveY = j;
  }

  public int getMoveX() {
    return moveX;
  }

  public void setMoveX(int moveX) {
    this.moveX = moveX;
  }

  public int getMoveY() {
    return moveY;
  }

  public void setMoveY(int moveY) {
    this.moveY = moveY;
  }

}
